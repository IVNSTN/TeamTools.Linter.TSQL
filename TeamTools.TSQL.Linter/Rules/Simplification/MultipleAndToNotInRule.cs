using Microsoft.SqlServer.TransactSql.ScriptDom;
using TeamTools.Common.Linting;
using TeamTools.TSQL.Linter.Routines;

namespace TeamTools.TSQL.Linter.Rules
{
    [RuleIdentity("SI0846", "MULTIPLE_AND_TO_NOT_IN")]
    internal sealed class MultipleAndToNotInRule : AbstractRule
    {
        private readonly CombinablePredicateExtractor predicateExtractor = new CombinablePredicateExtractor(true);

        public MultipleAndToNotInRule() : base()
        {
        }

        // A constraint and computed column definition must not use IN expression
        // unless DacFx fixes permanent diff generated by it.
        // See ConstraintWithoutListsRule
        public override void ExplicitVisit(CheckConstraintDefinition node)
        { }

        public override void ExplicitVisit(ColumnDefinition node)
        { }

        // Explicit - to avoid double visiting parts of complex AND-OR-AND-OR expression.
        public override void ExplicitVisit(BooleanBinaryExpression node)
        {
            if (node.BinaryExpressionType != BooleanBinaryExpressionType.And)
            {
                // Forwarding to base visitor so the nested binary expressions will be processed
                Visit(node);

                // Only AND is supported by the rule
                return;
            }

            predicateExtractor.Process(node, ViolationHandlerWithMessage);
        }
    }
}
