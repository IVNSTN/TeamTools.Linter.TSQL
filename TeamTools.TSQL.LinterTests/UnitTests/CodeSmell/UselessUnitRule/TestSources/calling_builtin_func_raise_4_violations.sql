CREATE FUNCTION dbo.foo(@title VARCHAR(100))
RETURNS NVARCHAR(100)
AS
BEGIN
    RETURN CAST(@title AS NVARCHAR(100))
END
GO

CREATE FUNCTION dbo.foo(@a INT, @b INT, @c INT)
RETURNS INT
AS
BEGIN
    RETURN COALESCE(@a, @b, @c)
END
GO

CREATE FUNCTION dbo.f_today()
RETURNS DATETIME
AS
BEGIN
    RETURN GETDATE()
END
GO

CREATE FUNCTION dbo.f_tomorrow()
RETURNS DATETIME
AS
BEGIN
    RETURN GETDATE() + 1
END
GO
